# ---------------------------------------------------------------
# convenience utilities
# ---------------------------------------------------------------

def total_levels() -> int:
    """Return the number of defined levels."""

    return len(LEVELS)


def get_level(idx: int) -> list[str]:


    if not 1 <= idx <= total_levels():
        raise IndexError(f"level {idx} is not defined")
    return LEVELS[idx - 1]


def validate_levels() -> bool:

    ok = True
    for i, lvl in enumerate(LEVELS, start=1):
        if not lvl:
            print(f"level {i} is empty")
            ok = False
            continue
        width = len(lvl[0])
        for j, row in enumerate(lvl, start=1):
            if len(row) != width:
                print(f"level {i} row {j} has inconsistent width")
                ok = False
    return ok


if __name__ == "__main__":
    print(f"defined {total_levels()} levels")
    if not validate_levels():
        print("level validation failed")
    else:
        print("all levels look good")
